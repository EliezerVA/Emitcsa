    public void cargarTabla(){
        
        DefaultTableModel modelo = (DefaultTableModel) jtblVehiculo.getModel();
        
        Vehiculo vei = new Vehiculo();
        List<Vehiculo> vei2 = new ArrayList<Vehiculo>();
        
        vei2 = vei.obtenerTo();
        
        for (Vehiculo vehiculo : vei2) {
            modelo.addRow(new Object[]{vehiculo.getModelo(),vehiculo.getPlaca(),vehiculo.getSocio()});
            
        }
       
        
    }

