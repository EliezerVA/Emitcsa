    public void cargarSocio(){
        
        if(cbxPropie.getSelectedItem().toString() == "-Selecciona-" ){
            
            JOptionPane.showMessageDialog(null, "Selecciona un socio");
            
            
        }else{
           List<Vehiculo> vei = new ArrayList<Vehiculo>();
        
        Vehiculo vei2 = new Vehiculo();
        
        vei = vei2.ObtenerPlaca(cbxPropie.getSelectedItem().toString());
        for (Vehiculo vehiculo : vei) {
           jlPlacas.setText(vehiculo.getPlaca());
            
        }
         
        }
        
        
    }

