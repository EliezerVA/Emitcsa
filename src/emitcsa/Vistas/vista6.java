    public void cargarTabla(){
        
        DefaultTableModel modelo = (DefaultTableModel) jtblSocio.getModel();
        
        Socio socio = new Socio();
        List<Socio> soci = new ArrayList<Socio>();
        
        soci = socio.obtenerTodo2();
        
        for (Socio socio1 : soci) {
            modelo.addRow(new Object[]{socio1.getNombre(),socio1.getTelefono(),socio1.getRFC(),socio1.getDomiFis()});
            
        }
           
            
        
       
        
    }
    
    

